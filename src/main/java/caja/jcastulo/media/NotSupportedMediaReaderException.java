package caja.jcastulo.media;

/**
 * 
 * @author Carlos Juarez
 */
public class NotSupportedMediaReaderException extends RuntimeException {

    public NotSupportedMediaReaderException() {
        super();
    }

    public NotSupportedMediaReaderException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public NotSupportedMediaReaderException(String arg0) {
        super(arg0);
    }

    public NotSupportedMediaReaderException(Throwable arg0) {
        super(arg0);
    }
}
